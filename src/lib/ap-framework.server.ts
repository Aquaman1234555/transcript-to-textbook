export const AP_FRAMEWORK_KNOWLEDGE = `# AP Framework — Analytical Lens

The AP Framework is a way of *understanding*, not summarizing. Most systems
compress information; this lens instead tries to surface assumptions, motives,
conditioning, and the deeper human question underneath a topic.

## Core stance
- Distinguish the real from the assumed. Notice what is taken for granted.
- Move from information toward wisdom. Facts are the surface; the question is
  "what does this mean for how a human should live, think, or act?"
- Treat the mind as conditioned. Beliefs, fears, desires, and identities shape
  what a speaker says and what an audience wants to hear.
- Inquiry over conclusion. Good analysis raises sharper questions, it does not
  manufacture neat answers.

## The four layers of content
For a given idea, separate (when meaningful):
1. **Information** — raw facts, data, "what happened / what is".
2. **Knowledge** — organized, explanatory understanding of the information.
3. **Insight** — a non-obvious realization that reframes the information.
4. **Wisdom** — guidance on living/acting rightly given the insight; touches
   values, freedom from compulsion, clarity of seeing.

Not every idea reaches wisdom. Do not invent a wisdom layer where there is only
information.

## Surface layer vs deep layer
- **Surface:** what is being said (the explicit claim).
- **Deep:** why it is being said — the motivation, the assumption it rests on,
  the fear/desire it appeals to, and the implication if taken seriously.

## Lenses to apply (only when relevant)
- **Hidden assumptions** — economic, psychological, social, cultural premises
  the speaker treats as obvious but never argues for.
- **Mental conditioning** — biases, inherited beliefs, fear-driven or ego-driven
  narratives, herd/identity pressures.
- **Human motivations & needs** — the underlying need (security, belonging,
  meaning, control, status, freedom) the topic is really about.
- **Fundamental questions** — Why does this matter? What human problem is being
  solved? What is the root cause behind this topic?
- **Potential blind spots** — what the framing omits, who it does not serve,
  where it could mislead.
- **Interdisciplinary connections** — links to psychology, philosophy, science,
  technology, society, human behavior, and learning theory.

## The non-imposition rule (critical)
Use the framework as a lens, not a template. If a video is a neutral technical
tutorial (e.g. how to install a library), there may be little hidden assumption
or conditioning to surface — and that is a valid finding. In such cases, keep
the AP analysis short and honest ("limited deeper subtext in this material")
rather than fabricating philosophy. Depth must be earned by the content.`;
